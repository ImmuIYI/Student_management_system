    private void searchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBActionPerformed
        // TODO add your handling code here:
        String st = searchtext2.getText();
        
        if(st.isEmpty()){
            JOptionPane.showMessageDialog(this, "Fill the Field");
            return;
        }
        
        try{
            //Connection
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/studentdetails","******","*********");
            
            PreparedStatement pstm = con.prepareStatement("SELECT * FROM details WHERE Rollno LIKE ? OR Name LIKE ? OR Marks LIKE ? OR Results LIKE ? OR Contact LIKE ? OR Address LIKE ?");
            
            
            String wildcardSearch = "%" + st + "%";
            for (int i = 1; i <= 6; i++) {
                pstm.setString(i, wildcardSearch);
            }
            
            ResultSet rs = pstm.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel)studentDetails.getModel();
            model.setRowCount(0);
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getInt("Rollno"),
                    rs.getString("Name"),
                    rs.getInt("Marks"),
                    rs.getString("Results"),
                    rs.getInt("Contact"),
                    rs.getString("Address")
                });
            }
            searchtext2.setText("");
            
            con.close();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_searchBActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = studentDetails.getSelectedRow();
        if (selectedRow != -1) {
            String selectedRollNo = studentDetails.getValueAt(selectedRow, 0).toString(); // Assuming roll number is in column 0
            new Aboutpage2(selectedRollNo).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Please select a student from the table.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

