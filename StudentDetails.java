    private void loadData(){
        try{
            //Connection
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/studentdetails","******","*********");
            
            PreparedStatement pstm = con.prepareStatement("SELECT * FROM details");
            ResultSet rs = pstm.executeQuery();
            
            DefaultTableModel model = (DefaultTableModel) studentDetails.getModel();
            model.setRowCount(0);
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getInt("Rollno"),
                    rs.getString("Name"),
                    rs.getInt("Marks"),
                    rs.getString("Results"),
                    rs.getInt("Contact"),
                    rs.getString("Address")
                });
            }
            con.close();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }

